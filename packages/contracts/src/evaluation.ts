import { z } from "zod";
import { StandardIncidentSchema } from "./incidents";

/**
 * High-level evaluation outcome returned by the evaluation service.
 */
export const EvaluationStatusValues = [
  "ok",
  "needs_attention",
  "critical",
] as const;

export const EvaluationStatusSchema = z.enum(EvaluationStatusValues);
export type EvaluationStatus = z.infer<typeof EvaluationStatusSchema>;

/**
 * Suggested action type returned by the evaluation service.
 * This is intentionally simple in v1 and can evolve later.
 */
export const SuggestedActionValues = [
  "monitor",
  "retry",
  "inspect_prompt",
  "inspect_schema",
  "check_provider",
  "escalate",
] as const;

export const SuggestedActionSchema = z.enum(SuggestedActionValues);
export type SuggestedAction = z.infer<typeof SuggestedActionSchema>;

/**
 * Request sent by the TypeScript core to the Python evaluation service.
 *
 * In v1, the request is centered around a single normalized incident.
 * Later versions may support batches, historical context, or trends.
 */
export const EvaluationRequestSchema = z.object({
  incident: StandardIncidentSchema,

  /**
   * Optional metadata about the request origin.
   */
  requestMeta: z
    .object({
      sourceSystem: z.string().min(1).optional(),
      requestedBy: z.string().min(1).optional(),
      correlationId: z.string().min(1).optional(),
    })
    .optional(),
});

export type EvaluationRequest = z.infer<typeof EvaluationRequestSchema>;

/**
 * Response produced by the Python evaluation service.
 *
 * The service interprets the incident and returns a first
 * operational assessment with reasoning and a suggested action.
 */
export const EvaluationResultSchema = z.object({
  status: EvaluationStatusSchema,

  /**
   * Numeric score representing impact / confidence / urgency.
   * We keep it simple in v1: 0-100.
   */
  score: z.number().min(0).max(100),

  /**
   * Human-readable summary of the evaluation result.
   */
  summary: z.string().min(1),

  /**
   * Optional explanation of why the incident was classified this way.
   */
  reasoning: z.string().min(1).optional(),

  /**
   * First actionable recommendation generated by the evaluation service.
   */
  suggestedAction: SuggestedActionSchema.optional(),

  /**
   * Optional list of tags to support filtering or future analytics.
   */
  tags: z.array(z.string().min(1)).optional(),
});

export type EvaluationResult = z.infer<typeof EvaluationResultSchema>;

